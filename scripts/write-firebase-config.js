// Writes docs/firebase-config.js from environment variables (used in CI).
// Required: FIREBASE_API_KEY, FIREBASE_PROJECT_ID, FIREBASE_APP_ID
// Optional: FIREBASE_AUTH_DOMAIN (defaults to {projectId}.firebaseapp.com)

import { writeFileSync } from "node:fs";

const apiKey = process.env.FIREBASE_API_KEY;
const projectId = process.env.FIREBASE_PROJECT_ID;
const appId = process.env.FIREBASE_APP_ID;
const authDomain =
  process.env.FIREBASE_AUTH_DOMAIN || `${projectId}.firebaseapp.com`;

const missing = [
  ["FIREBASE_API_KEY", apiKey],
  ["FIREBASE_PROJECT_ID", projectId],
  ["FIREBASE_APP_ID", appId],
].filter(([, v]) => !v);

if (missing.length) {
  console.error(
    "Missing required env vars:",
    missing.map(([k]) => k).join(", "),
  );
  process.exit(1);
}

const content = `// Generated by scripts/write-firebase-config.js — do not edit in CI.
// Local dev: edit docs/firebase-config.js directly or export the same env vars.

export const firebaseConfig = {
  apiKey: ${JSON.stringify(apiKey)},
  authDomain: ${JSON.stringify(authDomain)},
  projectId: ${JSON.stringify(projectId)},
  appId: ${JSON.stringify(appId)},
};

export const FIREBASE_SDK_VERSION = "12.14.0";

const isLocalhost =
  typeof location !== "undefined" &&
  (location.hostname === "localhost" || location.hostname === "127.0.0.1");

export const AUTH_EMULATOR_URL = isLocalhost ? "http://127.0.0.1:9099" : null;

export const FIRESTORE_EMULATOR = isLocalhost
  ? { host: "127.0.0.1", port: 8088 }
  : null;
`;

writeFileSync("docs/firebase-config.js", content);
console.log(`Wrote docs/firebase-config.js for project ${projectId}`);
